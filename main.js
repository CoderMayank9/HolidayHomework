function showParagraph1() {
    document.getElementById('paragraph-1').style.display = 'block';
}

function closeParagraph1() {
    document.getElementById('paragraph-1').style.display = 'none';
}

window.onclick = function(event) {
    if (event.target == document.getElementById('paragraph-1')) {
        closeParagraph1();
    }
}



